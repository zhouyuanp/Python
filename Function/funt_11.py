#测试参数的类型,位置参数 默认值参数 命名参数
def test01(a,b,c,d):
    print("{0}-{1}-{2}-{3}".format(a,b,c,d))
def test02(a,b,c=0,d=1):  #默认值参数,必须位于其他参数后面
    print("{0}-{1}-{2}-{3}".format(a,b,c,d))
test01(10,20,30,40) # 位置参数
#test01(10,20) # 参数个数不匹配,有报错
test01(a=50,c=20,d=30,b=40)# 命名参数

test02(10,20)
test02(10,20,30,40)






